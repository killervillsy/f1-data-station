function translateFromMap(translations: Record<string, string>, value: string): string {
  const normalizedValue = value.trim();
  const translated = translations[normalizedValue];

  if (translated) return translated;

  const lowerValue = normalizedValue.toLowerCase();
  const matchedKey = Object.keys(translations).find(
    (key) => key.toLowerCase() === lowerValue
  );

  return matchedKey ? translations[matchedKey] : value;
}

